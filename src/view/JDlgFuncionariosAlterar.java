    public FuncionariosEma viewBean() {
        FuncionariosEma funcionarios = new FuncionariosEma();
        int id = Util.strInt(jTxtId.getText());
        Date dataNascimento = Util.StrDate(jFmtDataNascimento.getText());
        Date dataRegistro = Util.StrDate(jFmtDataRegistro.getText());
        Double salario = Util.strDouble(jTxtSalario.getText());

        funcionarios.setIdFuncionarioEma(id);
        funcionarios.setDataContratacaoEma(dataRegistro);
        funcionarios.setNomeCompletoEma(jTxtNome.getText());
        funcionarios.setDataNascimentoEma(dataNascimento);

        if (jCboSexo.getSelectedIndex() == 1) {
            funcionarios.setGeneroEma("F");
        } else {
            funcionarios.setGeneroEma("M");
        }
        funcionarios.setCidadeEma(jTxtCidade.getText());
        funcionarios.setCepEma(jTxtCEP.getText());
        funcionarios.setTelefoneEma(jTxtTelefone.getText());
        funcionarios.setEmailEma(jTxtEmail.getText());
        funcionarios.setCargoEma(jTxtCargo.getText());
        funcionarios.setDepartamentoEma(jTxtDepartamento.getText());
        funcionarios.setSalarioEma(salario);
        funcionarios.setEnderecoEma(jTxtEndereco.getText());
        if (jCboAtivo.isSelected() == true) {
            funcionarios.setStatusEma("S");
        } else {
            funcionarios.setStatusEma("N");
        }

        return funcionarios;
    }

    public void beanView(FuncionariosEma funcionarios) {

        String intStr = Util.intStr(funcionarios.getIdFuncionarioEma());
        String dataNascimento = Util.dateStr(funcionarios.getDataNascimentoEma());
        String dataRegistroDate = Util.dateStr(funcionarios.getDataContratacaoEma());
        String salarios = Util.doubleStr(funcionarios.getSalarioEma());

        jTxtId.setText(intStr);
        jFmtDataRegistro.setText(dataRegistroDate);
        jTxtNome.setText(funcionarios.getNomeCompletoEma());
        jFmtDataNascimento.setText(dataNascimento);
        jTxtEmail.setText(funcionarios.getEmailEma());
        jTxtEndereco.setText(funcionarios.getEnderecoEma());
        jTxtCEP.setText(funcionarios.getCepEma());
        jTxtCidade.setText(funcionarios.getCidadeEma());
        jTxtTelefone.setText(funcionarios.getTelefoneEma());
        jTxtCargo.setText(funcionarios.getCargoEma());
        jTxtDepartamento.setText(funcionarios.getDepartamentoEma());
        jTxtSalario.setText(salarios);
        String sexo = funcionarios.getGeneroEma();
        if (sexo != null) {
            if (sexo.equals("Masculino")) {
                jCboSexo.setSelectedIndex(0);
            } else if (sexo.equals("Feminino")) {
                jCboSexo.setSelectedIndex(1);
            }
        } else {

        }

        if (funcionarios.getStatusEma().equals("S") == true) {
            jCboAtivo.setSelected(true);
        } else {
            jCboAtivo.setSelected(false);
        }

    }

