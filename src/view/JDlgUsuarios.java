    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        boolean confirmado = Util.mensagemConfirma("Confirmar excluir?");
        if (confirmado) {
            UsuariosEma usuarios = viewBean();
            DAO_Usuarios usuarios_DAO = new DAO_Usuarios();
            usuarios_DAO.delete(usuarios);
        } else {
            Util.mensagem("Exclusão cancelada");
        }

        Util.habilitar(false, jTxtId, jTxtNomeUsuario, jTxtNomeCompleto, jTxtEmail, jPwfSenha, jFmtDataRegistro, jFmtDataNascimento, jBtnCancelar, jBtnCancelar, jBtnConfirmar, jFmtDataRegistro, jBtnAlterar, jBtnExcluir);
        Util.habilitar(true, jBtnIncluir, jBtnPesquisar);
        Util.limparCampos(jTxtId, jTxtNomeUsuario, jTxtNomeCompleto, jTxtEmail, jPwfSenha, jFmtDataRegistro, jFmtDataNascimento);

    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        UsuariosEma usuarios = viewBean();

        DAO_Usuarios usuarios_DAO = new DAO_Usuarios();

        if (incluindo == true) {
            usuarios_DAO.insert(usuarios);
            Util.mensagem("Usuario adicionado com sucesso!");
        } else {
            usuarios_DAO.update(usuarios);
            Util.mensagem("Usuario alterado!");
        }

        Util.habilitar(false, jTxtId, jTxtNomeUsuario, jTxtNomeCompleto, jTxtEmail, jPwfSenha, jFmtDataRegistro, jFmtDataNascimento, jBtnCancelar, jBtnCancelar, jBtnConfirmar, jFmtDataRegistro, jBtnAlterar, jBtnExcluir);
        Util.habilitar(true, jBtnIncluir, jBtnPesquisar);
        Util.limparCampos(jTxtId, jTxtNomeUsuario, jTxtNomeCompleto, jTxtEmail, jPwfSenha, jFmtDataRegistro, jFmtDataNascimento);

    }//GEN-LAST:event_jBtnConfirmarActionPerformed

