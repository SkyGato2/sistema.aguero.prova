    public UsuariosEma viewBean() {
        UsuariosEma usuarios = new UsuariosEma();
        int id = Util.strInt(jTxtId.getText());
        Date dataNascimento = Util.StrDate(jFmtDataNascimento.getText());
        Date dataRegistro = Util.StrDate(jFmtDataRegistro.getText());

        usuarios.setIdUsuarioEma(id);
        usuarios.setNomeUsuarioEma(jTxtNomeUsuario.getText());
        usuarios.setNomeCompletoEma(jTxtNomeCompleto.getText());
        usuarios.setEmailEma(jTxtEmail.getText());
        usuarios.setSenhaEma(jPwfSenha.getText());
        usuarios.setDataNascimentoEma(dataNascimento);
        usuarios.setDataRegistroEma(dataRegistro);

        return usuarios;

    }

    public void beanView(UsuariosEma usuarios) {

        String intStr = Util.intStr(usuarios.getIdUsuarioEma());
        String dataNascimento = Util.dateStr(usuarios.getDataNascimentoEma());
        String dataRegistroDate = Util.dateStr(usuarios.getDataRegistroEma());

        jTxtId.setText(intStr);
        jFmtDataRegistro.setText(dataRegistroDate);
        jTxtNomeUsuario.setText(usuarios.getNomeUsuarioEma());
        jTxtNomeCompleto.setText(usuarios.getNomeCompletoEma());
        jTxtEmail.setText(usuarios.getEmailEma());
        jPwfSenha.setText(usuarios.getSenhaEma());
        jFmtDataNascimento.setText(dataNascimento);

    }

