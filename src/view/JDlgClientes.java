    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        boolean confirmado = Util.mensagemConfirma("Confirmar excluir?");
        if (confirmado) {
            ClientesEma cliente = viewBean();
            DAO_Clientes cliente_DAO = new DAO_Clientes();
            cliente_DAO.delete(cliente);
        } else {
            Util.mensagem("Exclusão cancelada");
        }

        Util.habilitar(false, jTxtId, jTxtNome, jFmtDataNascimento, jCboSexo, jFmtCpf, jTxtEmail, jTxtPais, jBtnAlterar, jTxtEstado, jTxtCidade, jTxtTelefone, jTxtTelefone, jTxtEstadoCivil, jTxtRg, jTxtProfissao, jTxtEndereco, jBtnCancelar, jBtnCancelar, jBtnConfirmar, jFmtDataRegistro, jBtnConfirmar, jBtnCancelar, jBtnExcluir);
        Util.habilitar(true, jBtnIncluir, jBtnPesquisar);
        Util.limparCampos(jTxtId,jTxtCEP, jTxtNome, jFmtCpf, jTxtPais, jCboSexo, jTxtEmail, jTxtTelefone,
                jTxtTelefone, jTxtCidade, jTxtEstado, jTxtRg, jTxtEstadoCivil, jTxtProfissao,
                jTxtEndereco, jFmtDataRegistro, jFmtDataNascimento);
    }//GEN-LAST:event_jBtnExcluirActionPerformed

    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        ClientesEma cliente = viewBean();

        DAO_Clientes cliente_DAO = new DAO_Clientes();

        if (incluindo == true) {
            cliente_DAO.insert(cliente);
            Util.mensagem("Cliente adicionado com sucesso!");
        } else {
            cliente_DAO.update(cliente);
            Util.mensagem("Cliente alterado!");
        }

        Util.habilitar(false,jTxtCEP, jTxtId, jTxtNome, jFmtDataNascimento, jCboSexo, jFmtCpf, jTxtEmail, jTxtPais, jTxtEstado, jTxtCidade, jTxtTelefone, jTxtTelefone, jTxtEstadoCivil, jTxtRg, jTxtProfissao, jTxtEndereco, jBtnCancelar, jBtnCancelar, jBtnConfirmar, jFmtDataRegistro, jBtnAlterar, jBtnExcluir);
        Util.habilitar(true, jBtnIncluir, jBtnPesquisar);
        Util.limparCampos(jTxtId,jTxtCEP ,jTxtNome, jFmtCpf, jTxtPais, jCboSexo, jTxtEmail, jTxtTelefone,
                jTxtTelefone, jTxtCidade, jTxtEstado, jTxtRg, jTxtEstadoCivil, jTxtProfissao,
                jTxtEndereco, jFmtDataRegistro, jFmtDataNascimento);


    }//GEN-LAST:event_jBtnConfirmarActionPerformed

