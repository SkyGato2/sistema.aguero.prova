    public ClientesEma viewBean() {
        ClientesEma clientes = new ClientesEma();
        int id = Util.strInt(jTxtId.getText());
        Date dataNascimento = Util.StrDate(jFmtDataNascimento.getText());
        Date dataRegistro = Util.StrDate(jFmtDataRegistro.getText());

        clientes.setIdClienteEma(id);
        clientes.setDataCadastroEma(dataRegistro);
        clientes.setNomeCompletoEma(jTxtNome.getText());
        clientes.setDataNascimentoEma(dataNascimento);
        clientes.setCpfEma(jFmtCpf.getText());
        clientes.setEmailEma(jTxtEmail.getText());
        clientes.setPaisEma(jTxtPais.getText());
        clientes.setEstadoEma(jTxtEstado.getText());
        clientes.setCidadeEma(jTxtCidade.getText());
        clientes.setCepEma(jTxtCEP.getText());
        clientes.setTelefoneEma(jTxtTelefone.getText());
        clientes.setRgEma(jTxtRg.getText());
        clientes.setEstadoCivilEma(jTxtEstadoCivil.getText());
        clientes.setProfissaoEma(jTxtProfissao.getText());
        clientes.setEnderecoEma(jTxtEndereco.getText());

        if (jCboSexo.getSelectedIndex() == 1) {
            clientes.setGeneroEma("Feminino");
        } else {
            clientes.setGeneroEma("Masculino");
        }

        return clientes;
    }

    public void beanView(ClientesEma clientes) {

        String intStr = Util.intStr(clientes.getIdClienteEma());
        String dataNascimento = Util.dateStr(clientes.getDataNascimentoEma());
        String dataRegistroDate = Util.dateStr(clientes.getDataCadastroEma());

        jTxtId.setText(intStr);
        jFmtDataRegistro.setText(dataRegistroDate);
        jTxtNome.setText(clientes.getNomeCompletoEma());
        jFmtDataNascimento.setText(dataNascimento);
        jFmtCpf.setText(clientes.getCpfEma());
        jTxtEmail.setText(clientes.getEmailEma());
        jTxtCEP.setText(clientes.getCepEma());
        jTxtEmail.setText(clientes.getEmailEma());
        jTxtEstado.setText(clientes.getEstadoEma());
        jTxtCidade.setText(clientes.getCidadeEma());
        jTxtTelefone.setText(clientes.getTelefoneEma());
        jTxtRg.setText(clientes.getRgEma());
        jTxtEstadoCivil.setText(clientes.getEstadoCivilEma());
        jTxtProfissao.setText(clientes.getProfissaoEma());
        jTxtEndereco.setText(clientes.getEnderecoEma());
        jTxtTelefone.setText(clientes.getCepEma());
        jTxtPais.setText(clientes.getPaisEma());

        String sexo = clientes.getGeneroEma();
        if (sexo != null) {
            if (sexo.equals("Masculino")) {
                jCboSexo.setSelectedIndex(0);
            } else if (sexo.equals("Feminino")) {
                jCboSexo.setSelectedIndex(1);
            }
        } else {

        }

    }

